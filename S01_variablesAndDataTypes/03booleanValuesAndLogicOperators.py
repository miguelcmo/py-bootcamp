# Use of a boolean operator inside a variable
x = (5 == 1)
print(x)
# Salida: False

# Use of boolean values directly in variables
x = True
if x:
    print("Python!")
# Salida: Python!

# Logic operators can be used directly on boolean values
print(True and False) # False
print(True or False)  # True
print(not True)       # False